def repeatedString(s, n):
    # Write your code here
    counting_a_in_s = s.count('a')
    cycle = n // len(s)
    remainder_n = n % len(s)
    counting_a_remainder = s[:remainder_n].count('a')

    return counting_a_in_s * cycle + counting_a_remainder

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    s = input()

    n = int(input().strip())

    result = repeatedString(s, n)

    fptr.write(str(result) + '\n')

    fptr.close()

